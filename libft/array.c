#include "minilibft.h"

void	ft_arrdel(void **arr)
{
	void	**tmp;

	tmp = arr;
	if (!arr)
		return ;
	while (*tmp)
	{
		free(*tmp);
		*tmp = 0;
		tmp++;
	}
	free(arr);
	arr = 0;
}
